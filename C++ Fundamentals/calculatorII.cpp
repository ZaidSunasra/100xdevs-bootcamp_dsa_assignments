#include <iostream>
using namespace std;

int main() {

    long long int n1, n2;

    cin >> n1;
    cin >> n2;

    cout << n1 << " + " << n2 << " = " << n1 + n2 << endl << endl;
    cout << n1 << " - " << n2 << " = " << n1 - n2 << endl << endl;
    cout << n1 << " * " << n2 << " = " << n1 * n2 << endl << endl;
    cout << n1 << " / " << n2 << " = " << n1 / n2 << endl << endl;
    cout << n1 << " % " << n2 << " = " << n1 % n2 << endl << endl;
}